// The point the user raised: a custom struct type flows through a wasm node on
// a single pin. Two Vector3f inputs (each a Make Vector3f output) feed the
// wasm AddVector3f, whose gau_read_/gau_write_ helpers see the struct leaves
// flattened into consecutive scalar slots; the struct output is reassembled.
static void TestWasmStructPins()
{
    const char* dir = "wasm_host_test_vec";
    std::error_code ec;
    std::filesystem::create_directories(dir, ec);
    {
        std::ofstream file(std::string(dir) + "/AddVector3f.wasm", std::ios::binary);
        file.write(reinterpret_cast<const char*>(ADDVEC_WASM), sizeof(ADDVEC_WASM));
    }
    std::vector<std::string> wasmErrors;
    const int loaded = WasmHost::Instance().LoadModulesFromDirectory(dir, wasmErrors);
    for (const std::string& error : wasmErrors) {
        std::printf("  wasm error: %s\n", error.c_str());
    }
    Check(loaded == 1, "AddVector3f module loaded");
    Check(WasmHost::Instance().HasFunction("AddVector3f"), "AddVector3f export found");

    TypeRegistry types;
    const TypeId f = types.Builtin(TypeTag::Float);
    StructDef vec;
    vec.name = "Vector3f";
    vec.fields = {{"x", f}, {"y", f}, {"z", f}};
    types.DefineStruct(vec);
    const TypeId vecId = types.UserType("Vector3f");

    NodeClassRegistry classes;
    classes.Register(Cls("MakeFloat", "Pure", {{PinDirection::Output, f, "Value"}},
                         {{"Value", f, Value::Float(0.0)}}, ""));
    classes.Register(Cls("AddVec", "Pure",
                         {{PinDirection::Input, vecId, "A"},
                          {PinDirection::Input, vecId, "B"},
                          {PinDirection::Output, vecId, "R"}},
                         {}, "wasm:AddVector3f"));

    BuiltinRegistry builtins;
    builtins.Register("MakeFloat", [](NodeEval& e) { e.Out(0, e.Prop(0)); });
    RegisterStructNodes(classes, builtins, types, vec);

    Graph g(types);
    const auto makeVec = [&](double x, double y, double z) -> PinId {
        const NodeId nx = g.AddNode(*classes.Find("MakeFloat"), 0, 0);
        const NodeId ny = g.AddNode(*classes.Find("MakeFloat"), 0, 0);
        const NodeId nz = g.AddNode(*classes.Find("MakeFloat"), 0, 0);
        const NodeId mk = g.AddNode(*classes.Find("Make Vector3f"), 0, 0);
        g.FindNode(nx)->properties[0] = Value::Float(x);
        g.FindNode(ny)->properties[0] = Value::Float(y);
        g.FindNode(nz)->properties[0] = Value::Float(z);
        g.AddLink(g.FindNode(nx)->outputs[0].id, g.FindNode(mk)->inputs[0].id);
        g.AddLink(g.FindNode(ny)->outputs[0].id, g.FindNode(mk)->inputs[1].id);
        g.AddLink(g.FindNode(nz)->outputs[0].id, g.FindNode(mk)->inputs[2].id);
        return g.FindNode(mk)->outputs[0].id;
    };

    const PinId aOut = makeVec(1.0, 2.0, 3.0);
    const PinId bOut = makeVec(10.0, 20.0, 30.0);
    const NodeId add = g.AddNode(*classes.Find("AddVec"), 0, 0);
    Check(g.CanConnect(aOut, g.FindNode(add)->inputs[0].id),
          "Vector3f output connects to wasm node's Vector3f input");
    g.AddLink(aOut, g.FindNode(add)->inputs[0].id);
    g.AddLink(bOut, g.FindNode(add)->inputs[1].id);

    Runtime rt(g, types, classes, builtins, [](const std::string&) {});
    const Value r = rt.EvalPin(g.FindNode(add)->outputs[0].id);
    const StructVal* sv = std::get_if<StructVal>(&r.data);
    Check(sv != nullptr && sv->fields.size() == 3, "wasm output reassembled as a Vector3f struct");
    if (sv != nullptr && sv->fields.size() == 3) {
        Check(sv->fields[0] == Value::Float(11.0), "R.x = 1+10 = 11");
        Check(sv->fields[1] == Value::Float(22.0), "R.y = 2+20 = 22");
        Check(sv->fields[2] == Value::Float(33.0), "R.z = 3+30 = 33");
    }
}

int main()
{
    TestWasmNodeEval();
    TestWasmStructPins();
    if (failCount == 0) {
        std::printf("wasm_host_tests: all passed\n");
    }
    return failCount == 0 ? 0 : 1;
}
